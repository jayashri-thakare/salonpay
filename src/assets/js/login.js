$(document).ready(function () {
    $("#loginForm").validate();
    $("#forgotForm").validate();

    $(document).on('click', '.forgot', function () {
        $('.login-container').removeClass('loginActive').addClass('forgetActive');
        $('.login-box').fadeOut(200);
        setTimeout(() => {
            $('.forgot-box').fadeIn(200);
        }, 220);
    });
    $(document).on('click', '.forget-login-link', function () {
        $('.login-container').removeClass('forgetActive').addClass('loginActive');
        $('.forgot-box').fadeOut(200);
        setTimeout(() => {
            $('.login-box').fadeIn(200);
        }, 220);
    });


});